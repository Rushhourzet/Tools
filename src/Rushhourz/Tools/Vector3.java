package Rushhourz.Tools;

public class Vector3{
    public double x;
    public double y;
    public double z;
    public Vector3(double x, double y, double z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
    public Vector3(int x, int y, int z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
    public Vector3(float x, float y, float z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
}